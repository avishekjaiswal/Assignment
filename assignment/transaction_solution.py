import sys
netAmount = 0
print("\nEnter the deposit and withdrawal values: ")
while True:
    s = raw_input()
    if not s:
        break
    values = s.split(" ")
    operation = values[0]
    amount = int(values[1])
    if operation=="D":
        netAmount+=amount
    elif operation=="W":
        netAmount-=amount
    else:
        pass
print ("The Net Amount is: {}".format(netAmount))